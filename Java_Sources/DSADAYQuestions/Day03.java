package DSADAYQuestions;

public class Day03 {
    static void findevenorodd(){
        int key = 6;
        if(key % 2 == 0){
            System.out.println(key + " is a even number");
        }
        else {
            System.out.println(key + " is a odd number");
        }
    }
    public static void main(String[] args) {
        findevenorodd();
    }
}
